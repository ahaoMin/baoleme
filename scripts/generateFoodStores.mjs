function pick(arr, i) {
  return arr[i % arr.length];
}

function buildStore(idNum, subcat, nameIdx) {
  const meta = CATEGORY_META[subcat];
  const pool = STORE_POOLS[subcat];
  const name = pool[nameIdx % pool.length];
  const dishes = DISH_TEMPLATES[subcat];
  const emojis = DISH_EMOJI[subcat];
  const categories = dishes.map((group, gi) => ({
    name: meta.catNames[gi] || `分类${gi + 1}`,
    items: group.map((dishName, di) => {
      const id = `r${idNum}d${gi * 10 + di + 1}`;
      const price = 12 + ((idNum + di * 3 + gi * 5) % 40);
      return {
        id,
        name: dishName,
        desc: '看起来很好吃（也只能看起来）',
        price,
        emoji: pick(emojis, di + gi),
        kcal: 200 + ((idNum * 7 + di * 13) % 600),
        sales: 200 + ((idNum * 11 + di * 17) % 3000),
      };
    }),
  }));

  return {
    id: `r${idNum}`,
    homeType: 'food',
    name,
    emoji: meta.emoji,
    rating: +(4.2 + ((idNum % 7) * 0.1)).toFixed(1),
    monthlySales: 800 + (idNum * 37) % 9000,
    deliveryTime: 20 + (idNum % 25),
    deliveryFee: idNum % 3 === 0 ? 0 : 2 + (idNum % 4),
    minOrder: 15 + (idNum % 5) * 5,
    distance: `${(0.8 + (idNum % 40) / 10).toFixed(1)}km`,
    tags: meta.tags,
    notice: pick(NOTICES, idNum),
    categories,
  };
}

function main() {
  const used = new Set();
  Object.values(EXISTING_BY_SUBCAT).flat().forEach((n) => used.add(n));

  const stores = [];
  let idNum = 22;
  const subcats = Object.keys(STORE_POOLS);
  const counters = Object.fromEntries(subcats.map((k) => [k, 0]));

  while (stores.length < 168) {
    for (const sub of subcats) {
      if (stores.length >= 168) break;
      const pool = STORE_POOLS[sub];
      let tries = 0;
      let name;
      do {
        name = pool[counters[sub] % pool.length];
        if (used.has(name)) {
          name = `${name}${Math.floor(counters[sub] / pool.length) + 2}号店`;
        }
        counters[sub] += 1;
        tries += 1;
      } while (used.has(name) && tries < 50);
      used.add(name);
      const store = buildStore(idNum, sub, counters[sub] - 1);
      store.name = name;
      stores.push(store);
      idNum += 1;
    }
  }

  const outPath = join(__dirname, '../src/data/foodStoresExtra.js');
  const body = `/** 自动生成：扩展外卖店铺 r22+，由 scripts/generateFoodStores.mjs 产出 */\nexport const EXTRA_FOOD_RESTAURANTS = ${JSON.stringify(stores, null, 2)};\n`;
  writeFileSync(outPath, body, 'utf8');
  console.log(`wrote ${stores.length} stores -> ${outPath}`);
}

main();
